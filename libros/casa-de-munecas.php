					<h3>Casa de Muñecas</h3>
					<cite>Henrik Ibsen</cite>
					<p class="texto-descripcion-libro">La acción de Casa de muñecas (1879) tiene como protagonista a Nora, una mujer que simula estúpida frivolidad pero que en verdad es inteligente y aguda. Nora se vio forzada años atrás a recurrir a una artimaña financiera para costear un viaje en salvaguarda de la salud de Helmer, su marido, al tiempo que su padre se hallaba moribundo. A medida que la tensión del conflicto va in crescendo, la monotonía burguesa de la protagonista se va convirtiendo en un nudo corredizo que va dejándola sin aire. Ante su impotencia, observa la imposición de todo un mundo de valores contradictorios que amenazan su idea de felicidad.</p>
