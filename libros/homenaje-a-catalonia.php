					<h3>Homenaje a Cataluña</h3>
					<cite>George Orwell</cite>
					<p class="texto-descripcion-libro">Orwell estuvo como miliciano del POUM en Cataluña desde el 26 de diciembre de 1936 hasta el 23 de junio de 1937, durante la Guerra civil española, y muestra la represión de la que fueron objeto los militantes del POUM por parte de los sectores estalinistas del gobierno republicano. A la vez de la lucha contra el avanze del fascismo en España al comienzo del alzamiento nacionalista. En ella, Orwell narra su llegada a una Barcelona que bulle en los últimos días de 1936 en la revolución libertaria, expresando su admiración por la misma en largos párrafos de su texto, con el fin de integrarse a una unidad combatiente del POUM.</p>
