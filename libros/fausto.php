					<h3>Fausto</h3>
					<cite>Johann Wolfgang von Goethe</cite>
					<p class="texto-descripcion-libro">Casi doscientos cincuenta años después del nacimiento de su autor, "Fausto" sigue siendo un clásico, pues plantea el problema de los límites y el sentido de la acción humana, y lo hace bajo la parábola del pacto con el diablo. La obra mantiene su vigencia hoy porque, mediante los interrogantes propios de la condición humana, reaviva la inquietud en una época que pretende sentirse ajena a ellos. En realidad, "Fausto "reúne dos obras distintas enmarcadas en momentos muy concretos de la historia europea: el posterior a la Revolución Francesa y el de la consiguiente Restauración monárquica.</p>
