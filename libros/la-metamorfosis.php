					<h3>La Metamorfosis</h3>
					<cite>Franz Kafka</cite>
					<p class="texto-descripcion-libro">«Al despertar Gregorio Samsa una mañana, tras un sueño intranquilo, se encontró en su cama convertido en un monstruoso insecto.» Tal es el abrupto comienzo, que nos sitúa de raíz bajo unas reglas distintas, de " La metamorfosis " , sin duda alguna la obra de Franz Kafka que ha alcanzado mayor celebridad. Escrito en 1912 y publicado en 1916, este relato es considerado una de las obras maestras del siglo xx por sus innegables rasgos precursores y el caudal de ideas e interpretaciones que desde siempre ha suscitado.</p>
