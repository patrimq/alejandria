					<h3>El Zoo de Cristal</h3>
					<cite>Tennessee Williams</cite>
					<p class="texto-descripcion-libro">El zoo de cristal retrata la vida de los Wingfield, una familia sureña: la madre, Amanda, obsesionada con salir de la pobreza y sacar adelante a su hija; la hija, Laura, una joven cuya leve discapacidad la ha transformado en un ser patológicamente inseguro, volcada exclusivamente al cuidado de sus figurillas de cristal; el hijo, Tom, joven ambicioso que se debate entre el deber de cuidar a su familia y el deseo de salir al mundo. El último personaje es Jim, un candidato de convencional buena apariencia, que representa todo lo que la familia ha deseado.</p>
