					<h3>Muchas Vidas, Muchos Maestros</h3>
					<cite>Brian Weiss</cite>
					<p class="texto-descripcion-libro">El doctor Brian Weiss —psiquiatra del hospital Mount Sinai de Miami— relata en este libro su experiencia con Catherine, una paciente a quien trató bajo hipnosis. En estado de trance profundo -solo un 15% de los hipnotizados logran llegar a este- Catherine recordó sus vidas anteriores y revivió sus traumas que eran la clave de sus constantes pesadillas y su ansiedad, comenzando cuando se llamaba Aronda, en Egipto, 18 siglos antes de Cristo. El escepticismo del doctor comenzó a diluirse cuando Catherine empezó a dar muestras de la existencia de "espacios entre una vida y otra," con sorprendentes acerca de la familia del Dr. Weiss y de su hijo fallecido.</p>
