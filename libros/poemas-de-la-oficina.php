					<h3>Poemas de la oficina</h3>
					<cite>Mario Benedetti</cite>
					<p class="texto-descripcion-libro">Mario Benedetti se entromete como un pequeño dios zumbante en los escritorios y en los dormitorios de los empleados y jefes, se oculta en los maletines o en la máquina de escribir, y dispara versos y preguntas. Estos Poemas de la oficina (1953-1956) y Poemas del hoyporhoy (1958-1961) nos invitan a redescubrir el misterio que está ahí, debajo de una pila de papeles, aplastados por la rutina.</p>
