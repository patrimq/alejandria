					<h3>Más allá del Bien y del Mal</h3>
					<cite>Friedrich Nietzsche</cite>
					<p class="texto-descripcion-libro">Si bien "Más allá del bien y del mal" (1886) retoma elementos e ideas de Así habló Zaratustra profundizando en ellos, presenta un tratamiento de los mismos completamente distinto. Entre una y otra obra hay, fundamentalmente, un reajuste de la mirada: el paso del símbolo al concepto, de la poesía a la psicología, de la confianza a la sospecha, de la lejanía que permite dejar de lado los defectos a la óptica microscópica que pone de relieve las miserias; un reajuste que permite a Friedrich Nietzsche (1844-1900) dar un paso más en la radicalidad de su pensamiento filosófico.</p>
